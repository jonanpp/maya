		function getBalance(element, area) {
			var id = element.find(":selected").data("id");
			var target = element.find(":selected").data("target");
			var value = element.val();
			
			if( id == "" && target == "" ) {
				setBalance(0);
				setLoading(false);
				return;
			}

			if( target != "" ) {
				var obj = ajaxReset;
				obj.url = '/api/wallet/'+ target +'/getBalance';
				if( target == "provider" ) {
					obj.method = "POST";
					obj.data = {pvcd: value};
				} else {
					obj.method = "GET";
					obj.data = null;
				}

				callApi3(obj.url, obj.method, obj.data).then(function(resp) {
					if( resp.success == true ) {
						setBalance(resp.data.balance, area);

						// Show and hide Withdraw Box
						if( isQualifiedToWithdraw() == true && parseInt(el.fromWallet.text()) > 0 ) {
							setWithdrawAllBox(true);
						} else {
							setWithdrawAllBox(false);
						}
						
						// get the bonus list
						if( isGrantedToCallBonus() == true ) {
							var TransferTo = el.transferTo.find("option:selected").data("id");
							getBonusList(TransferTo);
						} else {
							clearBonusList();
							renderBonus(false);
						}
					} else {
						setBalance(null, area);
					}

// 					isFromAndToQualified();
					obj = ajaxReset;
					setLoading(false);
				});
			}
		}
		
		function setBalance(balance, area) {
			var amount = balance != null ? formatCurrency(balance, null, 2) : alertMsg.retry;

			if( typeof area == "undefined" && area == false ) {
				$(".wallet_type_wrap").find("span.type_wallet").text(amount);
			} else {
				if( area == SELECT_AREA.TransferFrom ) {
					el.fromWallet.text(amount);
				} else {
					el.toWallet.text(amount);
				}
			}
		}

		function setBalanceToBox(code, balance) {
			var ele = $('h5[data-id="'+ code.toUpperCase() +'"]');

			if( !ele.next().is("span") ) {
				ele.next(".refreshcash").remove();
				ele.after("<span></span>");
			}
			ele.next("span").text(formatCurrency(balance, null, 2));
		}

		function isGrantedToCallBonus() {
			var From = parseInt(el.transferFrom.find("option:selected").data("id"));
			var To = parseInt(el.transferTo.find("option:selected").data("id"));
			var frWallet = el.fromWallet.text();
			var toWallet = el.toWallet.text();

			if( From == 0 && To > 0 && frWallet != alertMsg.retry && toWallet != alertMsg.retry ) {			
				return true;
			}
			return false;
		}
		
		function isQualifiedToWithdraw() {
			var From = parseInt(el.transferFrom.find("option:selected").data("id"));
			var To = parseInt(el.transferTo.find("option:selected").data("id"));
			
			if( From > 0 && To == 0 ) {
				if( parseInt(el.fromWallet.text()) > 0 ) {
					return true;
				}
				return false;
			}
			return false;
		}
		
		function getBonusList(pvid) {
			var obj = ajaxReset;
			obj.url = "/api/bonus/list";
			obj.method = "POST";
			obj.data = {idbtp: 2, idpv: pvid};

			callApi3(obj.url, obj.method, obj.data).then(function(resp) {
				if( resp.success == true ) {
					generateBonusList(resp.data.bonusLs);
				}
			});
		}

		function generateBonusList(data) {
			clearBonusList();
			
			var size = data.length;
			if( size > 0 ) {
				var html = el.bonusList.find(".conlist");
				html.append('<option value="" data-percentage="0" data-reward="0">- Pilih -</option>');
				for(var i = 0; i < size; i++) {
					var item = data[i];
					var id = item.id;
					var name = item.name;
					var reward = item.maximumValue;
					var percentage = item.percentage;
					var targetX = item.target;
					var text = name+', Bonus '+percentage+'% hingga '+formatCurrency(reward)+' ('+targetX+'x)';

					html.append('<option value="'+ id +'" data-percentage="'+ percentage +'" data-reward="'+ reward +'">'+ text +'</option>');
				}
				
				renderBonus(true);
			} else {
				renderBonus(false);
			}
		}
		
		function clearForm() {
			el.transferFrom.find("option:selected").removeAttr("selected");
			el.transferTo.find("option:selected").removeAttr("selected");
			el.fromWallet.text( formatCurrency(0, null, 2) );
			el.toWallet.text( formatCurrency(0, null, 2) );
		}
		
		function clearBonusList() {
			el.bonusList.find(".conlist").html("");
		}
		
		function renderBonus(isSet) {
			if( isSet == true ) {
				el.bonusWrapper.show();
				el.bonusPreviewBox.show();
				el.bonusPreview.val(0);
				return;
			}
			
			el.bonusWrapper.hide();
			el.bonusPreviewBox.hide();
			el.bonusPreview.val("");
		}
		
		function getSelectedBonus() {
			var bonusSelected = el.bonusList.find("option:selected");
			if( bonusSelected.length == 1 ) {
				return bonusSelected;
			}
			
			return null;
		}
		
		function countTotalReward(amount, percentage, maxReward) {
			amount = amount.replace(/\,/g, "");
			var total = parseFloat(amount) * (percentage / 100);

			return total > maxReward ? maxReward : total;
		}

		function setWithdrawAllBox(isSet) {
			isSet == true ? el.withdrawAllBox.show() : el.withdrawAllBox.hide();
		}

		function isFromAndToQualified() {
			if( el.fromWallet.text() == alertMsg.retry || el.toWallet.text() == alertMsg.retry ) {
				disableSubmitForm(true);
			} else {
				disableSubmitForm(false);
			}
		}

		function disableSubmitForm(isSet) {
			el.btnSubmit.prop("disabled", isSet);
			if( isSet == true ) {
				el.btnSubmit.addClass("disabled");
			} else {
				el.btnSubmit.removeClass("disabled");
			}
		}

		function setLoading(isSet) {
			isSet == true ? el.loader.show() : el.loader.hide();
		}
