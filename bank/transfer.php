			var formatted = formatCurrency(this.value, null, null, maxinput);
			
			if( el.withdrawAll.prop("checked") ) {
				el.withdrawAll.prop('checked', false);
				el.amount.val( formatCurrency(maxinput, null, 2) );
			}
			$(this).val( formatted );
			
			// calculating bonus value
			var bonusSelected = getSelectedBonus();
			if( bonusSelected != null ) {
				var percentage = parseFloat(bonusSelected.data("percentage"));
				var maxReward = parseFloat(bonusSelected.data("reward"));
				var reward = countTotalReward(this.value, percentage, maxReward);
				el.bonusPreview.val( formatCurrency(reward) );
			}
		});
		
		// WithdrawAll Checkbox
		el.withdrawAll.on("click", function() {
			var _this = $(this);
			var fromWallet = _this.prop("checked") ? el.fromWallet.text() : "";
			el.amount.val(fromWallet);
		});

