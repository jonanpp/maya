<script>
	$(document).ready(function() {
		init_main();

		function init_main() {
			var click = 0;
			$("#ReloadBalance").on("click", function() {
				click += 1;
				if( click == 1 ) {
					$(".page-loader").show();
					reload_balance();
					
					setTimeout(function() {
						click = 0;
					}, 2000);	// 2 secs
				}
			});
			
			function reload_balance() {
				callApi("/api/wallet/main/getBalance", null, function(response) {
					if( response.success == true ) {
						var amount = formatCurrency(response.data.balance, null, 2);
						$("#mainamt").text( amount + " K" );
						
						// hide the loader
						$(".page-loader").hide();
					}
				});
			}

			// Withdraw All Money
			$(".balance > .btn-withdraw").on("click", function(e) {
				e.preventDefault();
				if( confirm(alertMsg.confirmation_calibrate) ) {
					$(".page-loader").show();
					docalibrate();
				} else {
					return false;
				}
			});
			
			function docalibrate() {
				callApi("/api/wallet/calibrate", null, function(resp) {
					if( resp.success == true ) {
						reload_balance();
					}
				});
			}
		}
	});
</script>
